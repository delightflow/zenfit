// ===== 기본 무게 추천 알고리즘 =====

/**
 * 운동 기본 무게 계산
 *
 * 근거:
 *  - NSCA Essentials 4th ed. Table 17.7: 목표별 %1RM (lose=65%, gain=75%, maintain=70%)
 *  - Kraemer & Ratamess 2004 (ACSM, MSSE 36:674): 세트/렙/휴식 가이드라인
 *  - Appleby et al. 2013 (PMC3761768): bench 기반 예측식 (R²=0.76~0.86)
 *  - Strength Level 70만+ 리프트 데이터: 운동간 1RM 비율
 *  - Borges & Lino 2009 (PubMed 18978614): 스쿼트 기반 하지 예측식
 */
export function getDefaultWeight(
  exercise: Exercise,
  experience: 'beginner' | 'intermediate' | 'advanced' = 'beginner',
  oneRM?: { bench: number; squat: number; deadlift: number },
  goal: 'lose' | 'gain' | 'maintain' = 'maintain'
): number {
  if (['bodyweight', 'none', 'band'].includes(exercise.equipment)) return 0;

  // 목표별 작업 무게 비율 (NSCA Table 17.7)
  // lose: 15RM ≈ 65%1RM  /  gain: 10RM ≈ 75%1RM  /  maintain: 12RM ≈ 70%1RM
  const workRatio = goal === 'lose' ? 0.65 : goal === 'gain' ? 0.75 : 0.70;

  // 반올림 단위: 모든 장비 5kg (30, 35 같은 깔끔한 값)
  const roundTo = 5;

  if (oneRM) {
    // ── 하체: squat 기준 ──────────────────────────────────────────────────
    // Strength Level: 레그프레스 ≈ squat×1.74 (intermediate), 레그ext ≈ squat×0.48 (Borges 2009)
    // machine은 두 유형의 평균으로 squat×0.65 사용
    if (exercise.bodyPart === 'legs') {
      const legsExRatio =
        exercise.equipment === 'barbell' ? 1.00   // 스쿼트 변형 = squat 1RM 그대로
        : exercise.equipment === 'machine' ? 0.65  // 레그프레스/익스텐션/컬 혼합 평균
        : exercise.equipment === 'cable'   ? 0.40  // 케이블 킥백·RDL
        : 0.28;                                    // 덤벨 런지 (한 손 기준, Borges: 0.52×/2)
      const w = oneRM.squat * legsExRatio * workRatio;
      return Math.max(roundTo, Math.round(w / roundTo) * roundTo);
    }

    // ── 등·코어: deadlift 기준 / 나머지: bench 기준 ──────────────────────
    const ref = (exercise.bodyPart === 'back' || exercise.bodyPart === 'core')
      ? oneRM.deadlift
      : oneRM.bench;

    // 운동 1RM 추정 비율 (reference lift 1RM 대비)
    //
    // chest  (bench 기준)  barbell=1.00 / dumbbell=0.50per arm / machine=0.90 / cable=0.40
    // arms   (bench 기준)  barbell curl≈bench×0.47 (SL interm.), 트라이셉 푸시다운≈×0.58 → 평균 0.50
    //                     dumbbell: DB컬 per arm ≈ 바벨컬×0.50 → bench×0.24
    //                     cable: 트라이셉 푸시다운 기준 ≈ bench×0.50
    // shoulder(bench 기준) OHP≈bench×0.63 (SL; 60-70% 범위), DB숄더프레스≈bench×0.42 (Appleby 2013)
    //                     cable: 레터럴레이즈·페이스풀 ≈ bench×0.20
    // back   (DL 기준)    바벨로우≈DL×0.55 (SL: 0.51-0.59), 케이블로우≈바벨로우 (SL 거의 동일)
    //                     dumbbell row per arm ≈ barbell row×0.50 → DL×0.28
    //                     lat pulldown machine ≈ DL×0.45
    // core   (DL 기준)    전반적으로 가벼운 보조 운동
    const exRatioTable: Record<string, Partial<Record<string, number>>> = {
      chest:    { barbell: 1.00, dumbbell: 0.50, machine: 0.90, cable: 0.40 },
      arms:     { barbell: 0.50, dumbbell: 0.24, machine: 0.45, cable: 0.50 },
      shoulder: { barbell: 0.63, dumbbell: 0.42, machine: 0.55, cable: 0.20 },
      back:     { barbell: 0.55, dumbbell: 0.28, machine: 0.45, cable: 0.55 },
      core:     { barbell: 0.18, dumbbell: 0.12, machine: 0.18, cable: 0.18 },
    };

    const exRatio = exRatioTable[exercise.bodyPart]?.[exercise.equipment] ?? 0.40;
    const w = ref * exRatio * workRatio;
    return Math.max(roundTo, Math.round(w / roundTo) * roundTo);
  }

  // ── 1RM 없을 때 fallback: 경험 × 장비 × 부위 기준 무게 ──────────────
  const expMult = experience === 'intermediate' ? 1.3 : experience === 'advanced' ? 1.6 : 1.0;
  const baseWeights: Record<string, Partial<Record<BodyPart, number>>> = {
    barbell: { chest: 40, back: 50, legs: 60, shoulder: 30, arms: 25, core: 20, cardio: 0 },
    dumbbell: { chest: 15, back: 15, legs: 15, shoulder: 10, arms: 10, core: 10, cardio: 0 },
    machine: { chest: 35, back: 45, legs: 60, shoulder: 25, arms: 20, core: 20, cardio: 0 },
    cable:   { chest: 20, back: 30, legs: 30, shoulder: 15, arms: 15, core: 15, cardio: 0 },
  };
  const equipBase = baseWeights[exercise.equipment] ?? {};
  const base = equipBase[exercise.bodyPart] ?? 20;
  const raw = base * expMult;
  return Math.max(roundTo, Math.round(raw / roundTo) * roundTo);
}

/**
 * 점진적 과부하 (Progressive Overload)
 * NSCA Essentials 4th ed. + Schoenfeld 2016 기반
 * - 초보: 매 2세션마다 +5kg (빠른 신경 적응)
 * - 중급: 매 3세션마다 +5kg
 * - 고급: 매 4세션마다 +5kg
 * - 감량 목표: 증가 없음 (칼로리 적자 상태에서 근육 유지 우선)
 * - 유지 목표: 매 2주기마다 1회 증가 (느린 점진)
 *
 * @param currentWeight  현재 사용 무게 (kg)
 * @param completedSessions 오늘 포함 완료 세션 수
 * @returns 다음 세션에 사용할 무게 (kg)
 */
export function applyProgressiveOverload(
  currentWeight: number,
  completedSessions: number,
  experience: 'beginner' | 'intermediate' | 'advanced' = 'beginner',
  goal: 'lose' | 'gain' | 'maintain' = 'maintain',
): number {
  if (currentWeight === 0 || goal === 'lose') return currentWeight;
  const sessionInterval = experience === 'beginner' ? 2 : experience === 'intermediate' ? 3 : 4;
  if (completedSessions % sessionInterval !== 0) return currentWeight;
  // 유지 목표: 2 주기마다 1회만 증가
  if (goal === 'maintain') {
    const intervals = completedSessions / sessionInterval;
    if (intervals % 2 !== 0) return currentWeight;
  }
  return Math.round((currentWeight + 5) / 5) * 5;
}

// ===== 운동 프로그램 생성 =====

export interface SetDetail {
  weight: number; // kg, 0 for bodyweight
  reps: string;   // "12" or "30초"
  completed?: boolean;
}

export interface WorkoutPlanItem {
  exercise: Exercise;
  setDetails: SetDetail[]; // Per-set weight/reps (PlanFit style)
  restSeconds: number;
}

export interface WorkoutPlan {
  name: string;
  exercises: WorkoutPlanItem[];
  estimatedMinutes: number;
  estimatedCalories: number;
}

export function generateWorkoutPlan(
  goal: 'lose' | 'gain' | 'maintain',
  experience: 'beginner' | 'intermediate' | 'advanced',
  targetParts: BodyPart[]
): WorkoutPlan {
  const difficultyFilter: Difficulty[] =
    experience === 'beginner'
      ? ['beginner']
      : experience === 'intermediate'
        ? ['beginner', 'intermediate']
        : ['beginner', 'intermediate', 'advanced'];

  // Filter exercises by body parts and difficulty
  const available = exercises.filter(
    (e) => targetParts.includes(e.bodyPart) && difficultyFilter.includes(e.difficulty)
  );

  // 경험·목표별 볼륨 설정
  // Schoenfeld 2017 meta-analysis (Sports Med): MEV 5-10세트/주, MAV 12-20세트/주
  // RP Strength Volume Landmarks: 초보 MEV=4-6세트/주, 중급 MAV=12-20세트/주, 고급 16-20+세트/주
  // NSCA 4th ed.: 초보 2-3세트, 중급 3-4세트, 고급 4-6세트
  // → 세션당 총 세트: 초보 12세트(4×3) / 중급 15-20세트(5×3-4) / 고급 24-30세트(6×4-5)
  const volumeCfg: Record<string, Record<string, { exercises: number; sets: number; reps: string; restMult: number }>> = {
    beginner:     { lose: { exercises: 4, sets: 3, reps: '15', restMult: 0.75 }, gain: { exercises: 4, sets: 3, reps: '10', restMult: 1.25 }, maintain: { exercises: 4, sets: 3, reps: '12', restMult: 1.00 } },
    intermediate: { lose: { exercises: 5, sets: 3, reps: '15', restMult: 0.75 }, gain: { exercises: 5, sets: 4, reps: '10', restMult: 1.25 }, maintain: { exercises: 5, sets: 3, reps: '12', restMult: 1.00 } },
    advanced:     { lose: { exercises: 6, sets: 4, reps: '15', restMult: 0.75 }, gain: { exercises: 6, sets: 5, reps: '10', restMult: 1.25 }, maintain: { exercises: 6, sets: 4, reps: '12', restMult: 1.00 } },
  };
  const cfg = volumeCfg[experience]?.[goal] ?? volumeCfg.beginner.maintain;
  const count = cfg.exercises;
  const selected = shuffleAndPick(available, Math.min(count, available.length));

  const plan = selected.map((exercise) => {
    // 시간 기반 운동(초)은 기본값 유지, 나머지는 목표 렙으로 설정
    const reps = exercise.defaultReps.includes('초') ? exercise.defaultReps : cfg.reps;
    const sets = cfg.sets;
    const restSeconds = Math.round(exercise.restSeconds * cfg.restMult);

    const defaultWeight = getDefaultWeight(exercise, experience, undefined, goal);
    const setDetails: SetDetail[] = Array.from({ length: sets }, () => ({
      weight: defaultWeight,
      reps,
    }));

    return {
      exercise,
      setDetails,
      restSeconds,
    };
  });

  const totalSets = plan.reduce((sum, p) => sum + p.setDetails.length, 0);
  const estimatedCalories = plan.reduce((sum, p) => sum + p.exercise.caloriesPerSet * p.setDetails.length, 0);
  const estimatedMinutes = Math.round(totalSets * 1.5 + totalSets * (plan[0]?.restSeconds || 60) / 60);

  const goalNames = { lose: '체중 감량', gain: '근육 증가', maintain: '체력 향상' };

  return {
    name: `${goalNames[goal]} 워크아웃`,
    exercises: plan,
    estimatedMinutes,
    estimatedCalories,
  };
}

function shuffleAndPick<T>(arr: T[], count: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// ===== 분할 기반 결정적 플랜 생성 =====

export function generateSplitPlan(
  goal: 'lose' | 'gain' | 'maintain',
  experience: 'beginner' | 'intermediate' | 'advanced',
  split: 'A' | 'B' | 'C',
  options: {
    savedExerciseIds?: string[];
    savedWeights?: Record<string, number>;
    blacklist?: string[];
    oneRM?: { bench: number; squat: number; deadlift: number };
  } = {}
): WorkoutPlan {
  const splitDefs = SPLIT_DEFS[goal] || SPLIT_DEFS.maintain;
  const splitDef = splitDefs[split];
  const targetParts = splitDef.parts;

  const difficultyFilter: Difficulty[] =
    experience === 'beginner'
      ? ['beginner']
      : experience === 'intermediate'
        ? ['beginner', 'intermediate']
        : ['beginner', 'intermediate', 'advanced'];

  const blacklist = options.blacklist ?? [];

  // 사용 가능한 운동 필터링 (부위 + 난이도 + 블랙리스트 제외)
  const available = exercises.filter(
    (e) => targetParts.includes(e.bodyPart) &&
           difficultyFilter.includes(e.difficulty) &&
           !blacklist.includes(e.id)
  );

  // 경험·목표별 볼륨 설정 (Schoenfeld 2017 / RP Strength / NSCA 4th ed.)
  const volumeCfg: Record<string, Record<string, { exercises: number; sets: number; reps: string; restMult: number }>> = {
    beginner:     { lose: { exercises: 4, sets: 3, reps: '15', restMult: 0.75 }, gain: { exercises: 4, sets: 3, reps: '10', restMult: 1.25 }, maintain: { exercises: 4, sets: 3, reps: '12', restMult: 1.00 } },
    intermediate: { lose: { exercises: 5, sets: 3, reps: '15', restMult: 0.75 }, gain: { exercises: 5, sets: 4, reps: '10', restMult: 1.25 }, maintain: { exercises: 5, sets: 3, reps: '12', restMult: 1.00 } },
    advanced:     { lose: { exercises: 6, sets: 4, reps: '15', restMult: 0.75 }, gain: { exercises: 6, sets: 5, reps: '10', restMult: 1.25 }, maintain: { exercises: 6, sets: 4, reps: '12', restMult: 1.00 } },
  };
  const cfg = volumeCfg[experience]?.[goal] ?? volumeCfg.beginner.maintain;
  const count = cfg.exercises;
  let selected: Exercise[];

  if (options.savedExerciseIds && options.savedExerciseIds.length > 0) {
    // 저장된 운동 목록 사용 (블랙리스트 제외된 운동은 대체)
    selected = [];
    for (const id of options.savedExerciseIds) {
      if (blacklist.includes(id)) continue;
      const ex = exercises.find((e) => e.id === id);
      if (ex) selected.push(ex);
    }
    // 부족하면 추가
    if (selected.length < count) {
      const usedIds = new Set(selected.map((e) => e.id));
      const extras = available.filter((e) => !usedIds.has(e.id));
      // 결정적 정렬: 난이도 → 이름 순
      extras.sort((a, b) => a.difficulty.localeCompare(b.difficulty) || a.name.localeCompare(b.name));
      for (const ex of extras) {
        if (selected.length >= count) break;
        selected.push(ex);
      }
    }
  } else {
    // 첫 번째 생성: 결정적 정렬 (난이도 → 이름 순)으로 선택
    const sorted = [...available].sort(
      (a, b) => a.difficulty.localeCompare(b.difficulty) || a.name.localeCompare(b.name)
    );
    selected = sorted.slice(0, Math.min(count, sorted.length));
  }

  const plan = selected.map((exercise) => {
    const reps = exercise.defaultReps.includes('초') ? exercise.defaultReps : cfg.reps;
    const sets = cfg.sets;
    const restSeconds = Math.round(exercise.restSeconds * cfg.restMult);

    // 저장된 무게 우선, 없으면 1RM 기반(혹은 fallback) 계산
    const savedWeight = options.savedWeights?.[exercise.id];
    const defaultWeight = getDefaultWeight(exercise, experience, options.oneRM, goal);
    const weight = savedWeight ?? defaultWeight;

    const setDetails: SetDetail[] = Array.from({ length: sets }, () => ({
      weight,
      reps,
    }));

    return {
      exercise,
      setDetails,
      restSeconds,
    };
  });

  const totalSets = plan.reduce((sum, p) => sum + p.setDetails.length, 0);
  const estimatedCalories = plan.reduce((sum, p) => sum + p.exercise.caloriesPerSet * p.setDetails.length, 0);
  const estimatedMinutes = Math.round(totalSets * 1.5 + totalSets * (plan[0]?.restSeconds || 60) / 60);

  return {
    name: `${splitDef.label} 워크아웃`,
    exercises: plan,
    estimatedMinutes,
    estimatedCalories,
  };
}

// ===== 요일별 추천 부위 =====

export function getRecommendedParts(dayOfWeek: number, goal: 'lose' | 'gain' | 'maintain'): BodyPart[] {
  if (goal === 'lose') {
    const patterns: BodyPart[][] = [
      ['cardio', 'core', 'legs'],     // 일
      ['chest', 'arms', 'cardio'],     // 월
      ['legs', 'core', 'cardio'],      // 화
      ['back', 'shoulder', 'cardio'],  // 수
      ['cardio', 'core', 'legs'],      // 목
      ['chest', 'arms', 'cardio'],     // 금
      ['legs', 'back', 'core'],        // 토
    ];
    return patterns[dayOfWeek];
  }

  if (goal === 'gain') {
    const patterns: BodyPart[][] = [
      ['legs', 'core'],                // 일
      ['chest', 'arms'],               // 월
      ['back', 'shoulder'],            // 화
      ['legs', 'core'],                // 수
      ['chest', 'arms'],               // 목
      ['back', 'shoulder'],            // 금
      ['legs', 'arms', 'core'],        // 토
    ];
    return patterns[dayOfWeek];
  }

  // maintain
  const patterns: BodyPart[][] = [
    ['cardio', 'core'],              // 일
    ['chest', 'back', 'arms'],       // 월
    ['legs', 'cardio'],              // 화
    ['shoulder', 'arms', 'core'],    // 수
    ['cardio', 'legs'],              // 목
    ['chest', 'back'],               // 금
    ['core', 'cardio', 'legs'],      // 토
  ];
  return patterns[dayOfWeek];
}
